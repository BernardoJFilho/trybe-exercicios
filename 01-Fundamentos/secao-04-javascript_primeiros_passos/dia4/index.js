function determinaPositivoOuNegativo(parametro) {
  if (parametro > 0) {
    return "positivo";
  } else if (parametro < 0) {
    return "negativo";
  } else if (parametro === 0) {
    return "zero";
  } else {
    return "erro, digite novamente!";
  }
}

